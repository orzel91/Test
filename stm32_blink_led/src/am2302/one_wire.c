void ONE_WIRE_init(void)
{
	gpio_pin_cfg(AM2302_GPIO, AM2302_PIN, GPIO_CRx_MODE_CNF_OUT_OD_10M_value); // Set one wire pin
	AM2302_BB = 0; // Set pin in floating state
}

